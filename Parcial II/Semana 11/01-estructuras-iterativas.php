<?php

// Estructuras iterativas
echo "Estructura do while <br>";
$i=0;
do{
    echo "El valor del contador es: $i <br>";
    $i++;
}while($i<6);

echo "<br> Estructura while <br>";
$contador = 0;
while($contador<6){
    echo "El valor del contador es: $contador <br>";
    $contador ++;
}

echo "<br>Estructura for<br>";
for($i=0;$i<6;$i++){
     echo "El valor del contador es: $i <br>";
}

?>